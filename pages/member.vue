<script>
import { useAuth } from "@/store";

export default {
  name: "MemberView",
  components: {},
  data: () => ({
    member: [],
  }),
  methods: {
    compare(a, b) {
      if (a.name < b.name) {
        return -1;
      }
      if (a.name > b.name) {
        return 1;
      }
      return 0;
    },
    filterMemberByGen(gen) {
      let member = this.member.filter((a) => a.gen == gen);
      member.sort(this.compare);
      return member;
    },
    getMemberList() {
      $fetch(`${import.meta.env.VITE_API_URL}/member`, {
        method: "GET",
        headers: {
          Authorization: useAuth().$state.accessToken,
        },
      })
        .then((result) => {
          this.member = result;
        })
        .catch((result) => {});
    },
  },
  computed: {
    genList() {
      const gen = this.member.map((a) => a.gen);
      const set = new Set(gen);
      let uniqueGen = [...set];
      uniqueGen.sort();
      return uniqueGen;
    },
  },
  mounted() {
    this.getMemberList();
  },
};
</script>
